
# Inicialización de variables
# Este tipo de variables se conoce como `contador`
i = 0

# Sabemos que while se ejecuta mientras la condición sea verdadera
# Siempre debemos tener un seguro dentro del ciclo while para
# que no sea un ciclo infinito
while i < 10:
    print( f"Esto se mostrará 10 veces. Paso {i}" )
    i = i + 1
